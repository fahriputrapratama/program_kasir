    public static void viewDataProduk(String where) {
        try {
            DefaultTableModel m = (DefaultTableModel) tabeldata.getModel();
            m.getDataVector().removeAllElements();
            Connection K = Config.configDB();
            Statement S = K.createStatement();
            String Q = "SELECT * FROM produk "+where;
            ResultSet R = S.executeQuery(Q);
            int n = 1;
            while (R.next()) {                 
                int id = R.getInt("id_produk");
                String nama = R.getString("nama_produk");
                String stok = R.getString("stok");
                String supplier = R.getString("supplier");
                String kategori = R.getString("kategori");
                String hrgbeli = R.getString("harga_beli");
                String hrgjual = R.getString("harga_jual");
                Object[] data = {id,n,  nama, stok, supplier, kategori, hrgbeli, hrgjual};
                m.addRow(data);
                n++;
            }
            
            tabeldata.getColumnModel().getColumn(0).setMinWidth(0);
            tabeldata.getColumnModel().getColumn(0).setMaxWidth(0);
            
        } catch (Exception e) {
            //error handling
        }
    }
