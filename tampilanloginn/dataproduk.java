    private void btn_simpan1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpan1ActionPerformed
        // TODO add your handling code here:
        int id = Integer.parseInt(txtid1.getText());
        String name = txtnama1.getText();
        String stok = txtstok1.getText();
        String supplier = txtsupplier1.getText();
        String kategori = cmbkat1.getSelectedItem().toString();
        String hrgbeli = txthb1.getText();
        String hrgjual = txthj1.getText();
    
    // Query SQL untuk mengupdate data
       String query = "UPDATE produk SET nama_produk=?, stok=?, supplier=?, kategori=?, harga_beli=?, harga_jual=? WHERE id_produk = ?";
    
        try {
        // Membuat koneksi dan PreparedStatement
            Connection conn = Config.configDB();
            PreparedStatement PS = conn.prepareStatement(query);

            // Mengatur nilai pada query
            PS.setString(1, name);
            PS.setString(2, stok);
            PS.setString(3, supplier);
            PS.setString(4, kategori);
            PS.setString(5, hrgbeli);
            PS.setString(6, hrgjual);
            PS.setInt(7, id);    // ID yang menjadi acuan untuk update

            // Eksekusi query
            int res = PS.executeUpdate();

            // Periksa hasil eksekusi
            if (res > 0) {
                JOptionPane.showMessageDialog(null, "Data berhasil diubah!");
                viewDataProduk("");
                txtnama1.requestFocus();
            } else {
                JOptionPane.showMessageDialog(null, "Data gagal diubah!");
            }
        } catch (Exception e) {

        }
    }//GEN-LAST:event_btn_simpan1ActionPerformed

