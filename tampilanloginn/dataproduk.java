    private void btn_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpanActionPerformed
        // TODO add your handling code here:
        int id = Integer.parseInt(txtid.getText());
        String name = txtnama.getText();
        String stok = txtstok.getText();
        String supplier = txtsupplier.getText();
        String kategori = cmbkat.getSelectedItem().toString();
        String hrgbeli = txthb.getText();
        String hrgjual = txthj.getText();

        try {
            Connection K = Config.configDB(); // Pastikan koneksi ke database berhasil

            if (K == null) {
                JOptionPane.showMessageDialog(this, "Koneksi ke database gagal.");
                return; // Hentikan eksekusi jika koneksi gagal
            }

            String Q = "INSERT INTO produk (id_produk, nama_produk, stok, supplier, kategori, harga_beli, harga_jual) VALUES (?, ?, ?, ?, ?, ?, ?)";
            PreparedStatement PS = K.prepareStatement(Q);

            PS.setInt(1, id);
            PS.setString(2, name);
            PS.setString(3, stok);
            PS.setString(4, supplier);
            PS.setString(5, kategori);
            PS.setString(6, hrgbeli);
            PS.setString(7, hrgjual);

            PS.executeUpdate();

            // Tampilkan pesan berhasil
            JOptionPane.showMessageDialog(this, "Data berhasil disimpan");

            // Memanggil metode untuk memperbarui data di tabel jika sudah diimplementasikan
            viewDataProduk("");
            txtnama.requestFocus();

        } catch (Exception e) {

        }
    }//GEN-LAST:event_btn_simpanActionPerformed

